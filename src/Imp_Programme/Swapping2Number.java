package Imp_Programme;

public class Swapping2Number {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        System.out.println("Before Swapping the value: " +a+ " "+b);

        int t = a; // 10
        a = b; // 20
        b = t;
        System.out.println("After Swapping the value: " +a+ " " +b);

    }
}
