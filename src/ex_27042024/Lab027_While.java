package ex_27042024;

public class Lab027_While {
    public static void main(String[] args) {

        // While Loop
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
        // For loop in same above condition written
        System.out.println("Using For loop here");

        for(int j=1; j<=10; j++){
            System.out.println(j);
        }

    }
}


