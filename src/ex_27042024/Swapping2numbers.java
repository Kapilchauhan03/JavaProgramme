package ex_27042024;

public class Swapping2numbers {
    public static void main(String[] args) {
        int a =10;
        int b=20;
        System.out.println("Before Swapping the value: " +a+"  "+b);

        int t =a; // 10
        a =b; // 20
        b=t; // 10
        System.out.println("After Swapping the value "+a+ " "+b);


    }
}
